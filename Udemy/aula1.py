# Manipulacao de dadosS

num_int = 5
num_dec = 7.3
val_str = "qualquer texto"

print("O valor e:", num_int)
print("O valor e: %i" %num_int) # similar a C
print("O valor e: " + str(num_int)) #concatenate de strings

print("Dec: ", num_dec)
print("Dec: %.2f" %num_dec) # similar a C
print("Dec: " + str(num_dec))

print("String:",val_str)
print("String: %s" %val_str)
print("String: " + val_str)